package Before.j20220726;

public class Var_1 {

	public static void main(String[] args) {
		
		System.out.println("안녕하세요 반가워요.");
		
		String name = "이순신";
		String age = "24세";
		String address = "북구";
		
		System.out.println("이름은 : "+ name);
		System.out.println("나이는 : "+ age);
		System.out.println("주소는 : "+ address);
		
		name = "친구이름";
		age = "24세";
		address = "친구 주소";
		
		System.out.println("이름은 : "+ name);
		System.out.println("나이는 : "+ age);
		System.out.println("주소는 : "+ address);
	}
}

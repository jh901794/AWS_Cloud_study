package Before.j20220726;

public class Opt_1 {

	public static void main(String[] args) {
		int  init = 0;
//		++변수:  선 증가
		System.out.println(init);
		System.out.println(++init);
		System.out.println(init);
		System.out.println(++init);
		System.out.println(init++);
		System.out.println(init);
		System.out.println(init++);
		System.out.println(init++);
		System.out.println(++init);
	}

}
